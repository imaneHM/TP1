public class Animal {
    String nomAnimal;
    String typeAnimal;
    Animal(String nomAnim,String typeAnim){
        this.nomAnimal=nomAnim;
        this.typeAnimal=typeAnim;
    }
    public String getNomAnimal() {

        return this.nomAnimal;

    }
    public String getTypeAnimal() {

        return this.typeAnimal;

    }



}
